function renderArticles() {
  var tbody = document.getElementById('articlesBody');
  if (!tbody) return;
  
  var langFilter = document.getElementById('langFilter');
  var filterVal = langFilter ? langFilter.value : 'all';
  
  var filtered = ARTICLES;
  if(filterVal !== 'all') filtered = ARTICLES.filter(function(a){ return a.lang === filterVal; });
  
  document.getElementById('articleCount').textContent = filtered.length + ' статей';
  
  tbody.innerHTML = filtered.map(function(a) {
    var langClass = a.lang === 'UA' ? 'lang-ua' : a.lang === 'RU' ? 'lang-ru' : 'lang-en';
    return '<tr>' +
      '<td><strong>' + a.title + '</strong></td>' +
      '<td><span class="lang-badge ' + langClass + '">' + a.lang + '</span></td>' +
      '<td>' +
        '<a href="' + a.path + '" target="_blank" class="btn btn-outline btn-sm">Переглянути</a>' +
      '</td>' +
      '</tr>';
  }).join('');
}

